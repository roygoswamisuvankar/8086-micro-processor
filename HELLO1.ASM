;OUTPUT

;HELLO
;WORLD

.MODEL SMALL
.DATA
STR1 DB "HELLO$"  ;AUXILARY STRING1
STR2 DB "WORLD$"  ;AUXILARY STRING2
.CODE             ;CODE START

MOV AX,@DATA
MOV DS,AX
                    
MOV AH,09H          ;STRING PRINTING INSTRUCTIONS
MOV DX,OFFSET STR1  ;PRINT STRING ONE
INT 21H             ;INTURRUPT HANDLER

MOV AH,02H          ;NEW LINE INSTRUCTION
MOV DL,0DH          ;CARRAGE RETURN
INT 21H             ;INTURRUPT HANDLER
MOV DL,0AH          ;NEW LINE FEED
INT 21H             ;INTURRUPT HANDLER

MOV AH,09H          ;STRING PRINTING INSTRUCTIONS
MOV DX,OFFSET STR2  ;PRINT STRING TWO
INT 21H             ;INTURRUPT HANDLER

MOV AH,4CH          ;INSTRUCTION END
INT 21H             ;INTURRUPT HANDLER
END                 ;CODE END
