;PRINTING 'A' 

CODE SEGMENT
    ASSUME CS:CODE ;CODE START
    START:
    
    MOV AH,02H  ;INSTRUCTION START
    
    MOV DL,41H  ;HEXADECIMAL CODE OF 'A' MOVE OR STORE ON DL REGISTER
    INT 21H     ;INTURRUPT HANDLER
    
    MOV AH,4CH  ;INSTRUCTION END
    INT 21H     ;INTURRUPT HANDLER
    
    CODE ENDS   ;CODE END
END START
